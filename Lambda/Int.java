package Lambda;

/**
 * The Int class represents an integer value in the RPAL interpreter.
 * It extends the Rand class and provides a constructor to initialize the integer value.
 */
public class Int extends Rand {
    /**
     * Constructs a new Int object with the specified integer value.
     *
     * @param data the integer value to be stored in the Int object
     */
    public Int(String data) {
        super(data);
    }
}
