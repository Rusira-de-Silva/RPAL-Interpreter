package Lambda;

/**
 * The Rand class represents a random symbol.
 * It extends the Symbol class and provides methods to get the data of the symbol.
 */
public class Rand extends Symbol {
    /**
     * Constructs a new Rand object with the specified data.
     *
     * @param data the data of the symbol
     */
    public Rand(String data) {
        super(data);
    }
    
    /**
     * Returns the data of the symbol.
     *
     * @return the data of the symbol
     */
    @Override
    public String getData() {
        return super.getData();
    }
}
