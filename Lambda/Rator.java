package Lambda;

/**
 * The `Rator` class represents a symbol that acts as a rator in an RPAL expression.
 * It extends the `Symbol` class and provides a constructor to initialize the data of the symbol.
 */
public class Rator extends Symbol {
    /**
     * Constructs a new `Rator` object with the specified data.
     *
     * @param data the data of the rator symbol
     */
    public Rator(String data) {
        super(data);
    }
}
